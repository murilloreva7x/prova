
public class Disciplinas {
   
	private String nome;
	private String curso;
	private String serie;

    public Disciplinas (String nome, String curso, String serie ){
        this.nome = nome;
        this.curso = curso;
        this.serie= serie;
    }

    public String getNome(){
        return this.nome;
    }    
    public String getCurso(){
        return this.curso;
    }
    public String getSerie(){
        return this.serie;
    }   
}